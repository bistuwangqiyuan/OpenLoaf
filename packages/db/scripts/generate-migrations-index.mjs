/**
 * 构建时扫描 prisma/migrations/ 目录，生成 TypeScript 迁移索引文件。
 * 该文件会被 esbuild 打包进 server.mjs，运行时由 migrationRunner 读取。
 *
 * 用法: node packages/db/scripts/generate-migrations-index.mjs
 */
import crypto from 'node:crypto'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const dbRoot = path.resolve(__dirname, '..')
const migrationsDir = path.join(dbRoot, 'prisma', 'migrations')
const outputPath = path.join(dbRoot, 'src', 'migrations.generated.ts')

if (!fs.existsSync(migrationsDir)) {
  console.error(`[generate-migrations-index] No migrations directory at ${migrationsDir}`)
  process.exit(1)
}

const dirs = fs
  .readdirSync(migrationsDir)
  .filter((d) => {
    const full = path.join(migrationsDir, d)
    return fs.statSync(full).isDirectory() && fs.existsSync(path.join(full, 'migration.sql'))
  })
  .sort()

/** Strip Prisma-generated comment lines (e.g. `-- CreateTable`) from SQL. */
function stripSqlComments(raw) {
  return raw
    .split('\n')
    .filter((line) => !line.trimStart().startsWith('--'))
    .join('\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim()
}

const entries = dirs.map((name) => {
  const raw = fs.readFileSync(path.join(migrationsDir, name, 'migration.sql'), 'utf-8')
  const sql = stripSqlComments(raw)
  // checksum 基于原始文件，与 Prisma 自身记录的 checksum 保持一致
  const checksum = crypto.createHash('sha256').update(raw).digest('hex')
  return { name, sql, checksum }
})

const code = `/**
 * Auto-generated by scripts/generate-migrations-index.mjs
 * DO NOT EDIT MANUALLY
 */
import type { EmbeddedMigration } from './migrationRunner'

export const embeddedMigrations: readonly EmbeddedMigration[] = ${JSON.stringify(entries, null, 2)} as const
`

fs.writeFileSync(outputPath, code, 'utf-8')
console.log(`[generate-migrations-index] Generated ${entries.length} migrations → ${outputPath}`)
